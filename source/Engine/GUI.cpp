	ImGui_ImplSDL2_InitForSDLRenderer(Graphics::window.get(), Graphics::renderer.get());
	ImGui_ImplSDLRenderer2_Init(Graphics::renderer.get());

	//StyleClean();
	StyleCleanBlue();
	//StyleSteam();
